import getSpecial from '../app';

test('The function of object destructurization - getSpecial. Getting an array of objects with properties: id, name, description, icon', () => {
  const character = {
    name: 'Лучник',
    type: 'Bowman',
    health: 50,
    level: 3,
    attack: 40,
    defence: 10,
    special: [
      {
        id: 8,
        name: 'Двойной выстрел',
        icon: 'http://...',
        description: 'Двойной выстрел наносит двойной урон',
      },
      {
        id: 9,
        name: 'Нокаутирующий удар',
        icon: 'http://...',
      },
    ],
  };
  const expected = [
    {
      id: 8,
      name: 'Двойной выстрел',
      description: 'Двойной выстрел наносит двойной урон',
      icon: 'http://...',
    },
    {
      id: 9,
      name: 'Нокаутирующий удар',
      description: 'Описание недоступно',
      icon: 'http://...',
    },
  ];
  const received = getSpecial(character);

  expect(received).toEqual(expected);
});

test('The function of object destructurization - getSpecial. Passing an object without the special property', () => {
  const chrTest = {
    name: 'Лучник',
    type: 'Bowman',
    health: 50,
    level: 3,
    attack: 40,
    defence: 10,
  };
  const expected = [];
  const received = getSpecial(chrTest);

  expect(received).toEqual(expected);
});
